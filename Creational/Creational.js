/*
Creational Design Patterns 

- Factory Method 
    - Provides an interface for creating objects
    in a superclass, but allowws subclasses to alter
    the type of objects that will be created
- Abstract Factory
    - lets you produce families of related objects
    withour specifying their concrete classes.
- Builder
    - lets you construct complex objects step by step.
    The pattern allows you to produce different types
    and representations of an objext using the same
    construction code.
- Prototype
    - Lets you copy existing objects without
    making your code dependent on thier classes
- Singleton
    - Lets you ensure that a class only has one instance,
    while providing a global access point to this instance.
*/