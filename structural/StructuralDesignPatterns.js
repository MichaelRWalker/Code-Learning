/*
Structural Design Patterns 
- Adapter
    - Allows objects with incompatible interfaces to collaborate
- Bridge
    - Lets you split a large class or a set of closely
    related classes into two separate hierarchies-
    abstraction and implementation- which can be 
    developed independently of each other.
- Composite
    - Lets you compose objects into tree structures
    and then work with these structures as if they 
    were individual objects
- Decorator
    - Lets you attach new behaviors to objects by
    placing these objects inside special wrapper objects
    that contains the behaviors.
- Facade
    - Provides a simplefied interface to a library,
    a framework, or any other complex set of classes
- Flywieght
    - Lets you more objects into the available amount
    of ram by sharing common parts of state between multiple
    objects instead of keeping all of the data in each object
- Proxy
    - Lets you provide a substitute or placeholder for
    another object.
    A proxy controls acces to thje original object, 
    allowing you to preform something either before 
    or after the reques gets through to the original 
    object


*/
